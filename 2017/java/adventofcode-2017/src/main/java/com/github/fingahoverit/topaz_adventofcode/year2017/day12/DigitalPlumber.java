	public Integer partTwo() {

		Map<Integer, Set<Integer>> distributed = distributeInput();

		Set<Integer> overall = new HashSet<>();
		Integer count = 0;
		for (Integer program : distributed.keySet()) {

			if (!overall.contains(program)) {
				Set<Integer> inTouchSet = new HashSet<>();
				inTouchSet = populateIntouchSet(distributed, inTouchSet, program);

				count++;
				overall.addAll(inTouchSet);
			}
		}

		return count;
	}

	private Set<Integer> populateIntouchSet(Map<Integer, Set<Integer>> distributed, Set<Integer> inTouchSet,
			Integer i) {

		inTouchSet.add(i);
		for (Integer inTouch : distributed.get(i)) {
			if (!inTouchSet.contains(inTouch)) {
				inTouchSet.addAll(populateIntouchSet(distributed, inTouchSet, inTouch));
			}
		}

		return inTouchSet;
	}

	private Map<Integer, Set<Integer>> distributeInput() {

		Map<Integer, Set<Integer>> distributed = new HashMap<>();

		for (String line : input.split("\r\n")) {

			DataLine dataLine = datafy(line);

			if (distributed.get(dataLine.getProgram()) != null) {
				dataLine.friends.forEach(friend -> distributed.get(dataLine.getProgram()).add(friend));
			} else {
				distributed.put(dataLine.getProgram(), dataLine.friends.stream().collect(Collectors.toSet()));
			}

			dataLine.friends.forEach(friend -> {
				if (distributed.get(friend) == null) {
					distributed.put(friend, new HashSet<Integer>());
				}
				distributed.get(friend).add(dataLine.getProgram());
			});
		}

		// for (Integer program : distributed.keySet()) {
		// System.out.println(program + ">>>" +
		// Arrays.toString(distributed.get(program).toArray()));
		// }

		return distributed;
	}

	private DataLine datafy(String line) {
		DataLine dataLine = new DataLine();

		String[] lineArray = line.split(" <-> ");
		String[] friendsArray = lineArray[1].split(", ");

		dataLine.setProgram(Integer.valueOf(lineArray[0]));
		dataLine.setFriends(
				Arrays.asList(friendsArray).stream().map(i -> Integer.valueOf(i)).collect(Collectors.toList()));

		return dataLine;
	}

